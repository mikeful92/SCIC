<h3>I have Medicare, why do I need a Medicare Supplement policy also?</h3>
<p>Medicare was not designed to cover all medical expenses. Medicare Supplement policies vary in design to meet individual needs and budgets.</p>
<h3>Will my doctors accept a Medicare Medicare Supplement policy?</h3>
<p>Yes! Any doctor who accepts Medicare automatically accepts  Medicare Supplement policies.</p>
<h3>Do I need a referral to see a specialist?</h3>
<p>No. Medicare does not require a referral to see a specialist. You may obtain care from primary care physicians and specialists without the delays of referrals and pre-authorizations.</p>
<h3>How does my coverage change when I travel?</h3>
<p>It doesn't! Freedom is one of the biggest advantages of a  Medicare Supplement policy. You can receive care anywhere in the U.S. Unlike many "managed care" plans, you are not restricted to "Emergency Care Only" outside of your area.</p>
<h3>Can a Medicare Supplement policy end my coverage?</h3>
<p>No. Individually owned  Medicare Supplement policies cannot be cancelled or changed as long as the premium is paid. Medicare Supplement policies have provided permanent, stable benefits to millions of satisfied  policyholders since 1966.</p>
